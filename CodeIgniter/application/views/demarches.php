    <div class="container-lg text_section ecole_section" id="permis-de-construire">
        <h3>Permis de construire</h3>
        <p>Un permis de construire est exigé :<br>
            <br>
            - Pour les travaux créant une nouvelle construction, c’est-à-dire une construction indépendante du bâti existant sur la propriété, et qui ajoutent une surface plancher ou une emprise au sol supérieure à 40m².<br>
            <br>
            - Pour les travaux sur construction existante qui ajoutent entre 20 et 40 m² de surface plancher ou d’emprise au sol et qui portent la surface totale de la construction à plus de 150m².<br>
            <br>
            Le recours à l’architecte est obligatoire si le projet a une surface plancher supérieur à 150m².<br>
            <br>
            Pour plus d’informations, cliquer ici.</p>
    </div>
    <div class="container-lg text_section ecole_section" id="declaration_travaux">
        <h3>Declaration de travaux</h3>
        <p>La déclaration de travaux est un document administratif obligatoire pour les travaux de faible importance.<br>
            <br>
            Elle doit être effectuée :<br>
            <br>
            - pour tout projet de construction nouvelle ou d'agrandissement d'une surface hors œuvre brute (SHOB) comprise entre 2m² et 20m²<br>
            <br>
            - lors de la transformation de plus de 10m² de SHOB en SHON (surface hors œuvre nette)<br>
            <br>
            - lors de la modification de l'aspect extérieur d'un bâtiment<br>
            <br>
            - lors d'un changement d'affectation</p>
    </div>
